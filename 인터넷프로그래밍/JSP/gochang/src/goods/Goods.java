package goods;

public class Goods {
	private int gdID;
	private String gdName;
	private String gdContent;
	private int gdPrice;
	private String gdDate;
	private String gdImage;
	public int getGdID() {
		return gdID;
	}
	public void setGdID(int gdID) {
		this.gdID = gdID;
	}
	public String getGdName() {
		return gdName;
	}
	public void setGdName(String gdName) {
		this.gdName = gdName;
	}
	public String getGdContent() {
		return gdContent;
	}
	public void setGdContent(String gdContent) {
		this.gdContent = gdContent;
	}
	public int getGdPrice() {
		return gdPrice;
	}
	public void setGdPrice(int gdPrice) {
		this.gdPrice = gdPrice;
	}
	public String getGdDate() {
		return gdDate;
	}
	public void setGdDate(String gdDate) {
		this.gdDate = gdDate;
	}
	public String getGdImage() {
		return gdImage;
	}
	public void setGdImage(String gdImage) {
		this.gdImage = gdImage;
	}
	

}
